"""
Answer evaluation router.
"""

from __future__ import annotations

import logging

from fastapi import APIRouter, HTTPException

from ai_ml.exceptions import EvaluationError
from app.schemas.evaluation import EvaluateAnswer, EvaluateAnswerResponse
from app.services.evaluation_service import evaluate_answer

logger = logging.getLogger(__name__)

router = APIRouter(prefix="/evaluate", tags=["Evaluation"])


@router.post(
    "/answer",
    response_model=EvaluateAnswerResponse,
    summary="Evaluate a student's answer",
    description=(
        "Submit a question, student answer, rubric, and maximum marks. "
        "Returns a score, strengths, weaknesses, justification, and "
        "improvement suggestions generated by Gemini."
    ),
)
async def evaluate_answer_endpoint(payload: EvaluateAnswer) -> EvaluateAnswerResponse:
    """
    Evaluate a long-form / viva answer using the AI evaluation engine.

    - ``rubric`` can be a single string or a list of criteria.
    - If the student answers "I don't know", the score will be 0.
    """
    try:
        return evaluate_answer(payload)
    except EvaluationError as exc:
        logger.error("Evaluation error for question_id=%s: %s", payload.question_id, exc)
        raise HTTPException(status_code=500, detail=f"Evaluation failed: {exc}")
    except Exception:
        logger.exception("Unexpected evaluation error for question_id=%s", payload.question_id)
        raise HTTPException(status_code=500, detail="Evaluation failed due to an internal error.")
